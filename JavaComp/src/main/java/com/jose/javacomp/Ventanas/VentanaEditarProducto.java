    public VentanaEditarProducto(JFrame lastFrame, Integer ident) {
        initComponents();
        
        this.lastFrame = lastFrame;
        //El tipo (int) no puede establecerse a null al ser primitivo, pero el tipo Integer si
        this.ident = ident;
        
        if (ident == null){
            //Añadimos nuevo producto
            tituloVentanaLabel.setText("Añadir nuevo producto");
            identTF.setText("No generado aún");
        }
        else{
            //Editamos producto ya existente
            Producto p = JavaComp.prod.get(ident);
            
            identTF.setText(String.valueOf(ident));
            tituloTF.setText(p.getTitulo());
            descTA.setText(p.getDesc());
            stockSpinner.setValue(p.getStock());
            precioSpinner.setValue(p.getPrecio());
            categoriaCB.setSelectedIndex(Utilidades.convertirCategoriaIndice(p.getCategoria()));
            ImageIcon img = new ImageIcon(p.getImagen());
            imagenLabel.setIcon(img);
            if (img != null)
                imagenLabel.setText("");
            else
                imagenLabel.setText("Vista previa");
        }
    }

