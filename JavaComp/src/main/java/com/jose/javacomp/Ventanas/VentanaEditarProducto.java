    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        String titulo = tituloTF.getText();
        String desc = descTA.getText();
        Categorias categoria = Utilidades.convertirIndiceCategoria(categoriaCB.getSelectedIndex());
        float precio;
        try{ precio = (float)precioSpinner.getValue(); }
        catch (Exception e){
            errorRegistro("Precio del producto no válido");
            return;
        }
        int stock ;
        try{ stock = (int)stockSpinner.getValue(); }
        catch (Exception e){
            errorRegistro("Número de stock no válido");
            return;
        }
        
        if (ident == null){
            int newIdent = Utilidades.generarIdentificacion();
            LocalDate fecha = LocalDate.now();
            
            JavaComp.prod.put(newIdent, new Producto(titulo, desc, categoria, precio, imgPath, stock, fecha, new HashMap<Integer, Opinion>()));
        }
        else{
            Producto p = JavaComp.prod.get(ident);
            
            p.setTitulo(titulo);
            p.setDesc(desc);
            p.setCategoria(categoria);
            p.setPrecio(precio);
            p.setImagen(imgPath);
            p.setStock(stock);
        }
        
        this.dispose();
        lastFrame.setVisible(true);
    }//GEN-LAST:event_confirmarButtonActionPerformed

