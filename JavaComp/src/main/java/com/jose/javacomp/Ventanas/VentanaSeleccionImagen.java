    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        File imgFile = new File("Imagenes/");
        if (imgFile.exists()){
            
        DefaultTableModel dtm = (DefaultTableModel)imgTable.getModel();
            for (File f : imgFile.listFiles()){
                try{
                    dtm.addRow(new Object[]{
                    Utilidades.escalarImagen(f.getPath(), 100, 100),
                    f.getPath()
                    });
                }
                catch (Exception e){
                    System.out.println("No se pudo cargar la imagen: " + f.getName());
                }
            }
        }
        else{
            imgFile.mkdirs();
        }
    }//GEN-LAST:event_formComponentShown

