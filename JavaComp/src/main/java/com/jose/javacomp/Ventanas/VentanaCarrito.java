    private void procesarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procesarButtonActionPerformed
        LocalDateTime fecha = LocalDateTime.now();
        String factura = fecha.format(DateTimeFormatter.ofPattern("dd-MM-yyyy_HH-mm-ss_SSS")) + ".txt";
        
        try{
            File f = new File("Facturas/");
            if (!f.exists())
                f.mkdirs();
            FileOutputStream fos = new FileOutputStream("Facturas/factura_" + factura);
            PrintWriter s = new PrintWriter(fos);
            
            s.println("-----------------------");
            s.print("Factura del: " + fecha.format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss")));
            s.println("-----------------------");
            
            int i = 1;
            for (ProductoCarrito pc : c.getCarrito()){
                s.println(i++ + ":");
                s.println(" Producto: " + pc.getTitulo());
                s.println(" Descripci贸n: " + pc.getDesc());
                s.println(" Cantidad: " + pc.getCantidad());
                s.println(" Precio: " + pc.getPrecio());
                s.println("");
            }
            
            fos.close();
            s.close();
        }
        catch(Exception e){
            System.out.println("No se pudo guardar la factura");
            e.printStackTrace();
        }
        
        c.getCarrito().clear();
        JOptionPane.showMessageDialog(this, 
                "Productos comprados correctamente. Se ha generado una factura con el nombre " + factura,
                "Operaci贸n completada",
                JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        lastFrame.setVisible(true);
    }//GEN-LAST:event_procesarButtonActionPerformed

